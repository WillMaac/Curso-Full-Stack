const numeros = [1, 2, 3, 4, 5];

for (const numero of numeros) {
    const quadrado = numero ** 2;
    console.log(`Número: ${numero}, Quadrado: ${quadrado}`);
}
// Saída:
// Número: 1, Quadrado: 1
// Número: 2, Quadrado: 4
// Número: 3, Quadrado: 9
// Número: 4, Quadrado: 16
// Número: 5, Quadrado: 25
